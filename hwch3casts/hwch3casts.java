package hwch3casts;

public class Casts {
	public static void main (String [] args) {

		long x = 1L;
		System.out.println(x);
		int x1 = (int) x;
		System.out.println(x1);

		double c = 20.4;
		System.out.println(c);
		double c1 = (int) c;
		System.out.println(c1);

		byte d = 10;
		System.out.println(d);
		byte d1 = (byte) d;
		System.out.println(d1);
	}
}